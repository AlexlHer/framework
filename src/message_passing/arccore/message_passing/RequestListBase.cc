void RequestListBase::
removeDoneRequests()
{
  // TODO: optimiser cela en supprimant plusieurs valeurs d'un coup
  for( Integer i=0, n=m_requests.size(); i<n; ++i ){
    if (m_requests_done[i]){
      _removeRequestAtIndex(i);
      --i;
      --n;      
    }
  }
}

