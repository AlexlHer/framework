/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

Communicator::_Communicator Communicator::null_communicator;
int Communicator::null_communicator_type = Communicator::T_Null;

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void Communicator::
print(std::ostream& o) const
{
  if (m_type==T_Null)
    o << "(null)";
  else if (m_type==T_Int)
    o << m_communicator.i;
  else if (m_type==T_Long)
    o << m_communicator.l;
  else
    o << m_communicator.cv;
}

