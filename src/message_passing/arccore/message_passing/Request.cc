MessagePassing::Request::_Request MessagePassing::Request::null_request;

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void MessagePassing::Request::
print(std::ostream& o) const
{
  if (m_type==T_Null)
    o << "(null)";
  else if (m_type==T_Int)
    o << m_request.i;
  else if (m_type==T_Long)
    o << m_request.l;
  else
    o << m_request.cv;
}

