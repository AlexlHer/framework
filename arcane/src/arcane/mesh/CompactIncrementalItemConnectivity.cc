CompactIncrementalItemConnectivity::
CompactIncrementalItemConnectivity(ItemFamily* source_family,
                                   IItemFamily* target_family,
                                   const String& aname)
: AbstractIncrementalItemConnectivity(source_family,target_family,aname)
, m_true_source_family(source_family)
, m_pre_allocated_size(0)
{
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

CompactIncrementalItemConnectivity::
~CompactIncrementalItemConnectivity()
{
}

