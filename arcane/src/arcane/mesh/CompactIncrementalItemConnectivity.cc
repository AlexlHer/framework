/*---------------------------------------------------------------------------*/

void CompactIncrementalItemConnectivity::
notifySourceItemAdded(ItemLocalId item_local_id)
{
  ARCANE_UNUSED(item_local_id);
  m_items_internal = m_true_source_family.itemsInternal();
}

/*---------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------*/

void CompactIncrementalItemConnectivity::
notifyReadFromDump()
{
  m_items_internal = m_true_source_family.itemsInternal();
}

