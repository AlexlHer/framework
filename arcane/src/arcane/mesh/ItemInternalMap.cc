void ItemInternalMap::
notifyUniqueIdsChanged()
{
  BaseClass& c = *this;
  ENUMERATE_ITEM_INTERNAL_MAP_DATA(nbid,c){
    nbid->setKey(nbid->value()->uniqueId().asInt64());
  }
  this->rehash();
}

