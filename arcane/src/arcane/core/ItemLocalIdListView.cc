bool operator==(const ItemLocalIdListView& lhs, const ItemLocalIdListView& rhs)
{
  Int32 size1 = lhs.size();
  Int32 size2 = rhs.size();
  if (size1 != size2)
    return false;
  for (Int32 i = 0; i < size1; ++i)
    if (lhs[i] != rhs[i])
      return false;
  return true;
}

