//! Opérateur de sortie sur un flot
extern "C++" ostream&
operator<< (ostream& o,eOperation operation)
{
  switch(operation){
  case eOperation::Add:
    o << "Add";
    break;
  case eOperation::Remove:
    o << "Remove";
    break;
  default:
    o << "Unknown";
  }
  return o;
}


